    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pass = jPasswordField1.getText();
        String cpass = jPasswordField2.getText();
        String username = jTextField7.getText();
        String phoneNum = jTextField1.getText();
        String email = jTextField2.getText();
        if (!pass.equals("") && !cpass.equals("") && !username.equals("") && !phoneNum.equals("") && !email.equals("")) {
            if (pass.length() < 8 && cpass.equals(pass) && phoneNum.length() < 10) {
                new Login().setVisible(true);
                new Signup().dispose();
            } else if (pass.length() >= 8) {
                JOptionPane.showMessageDialog(rootPane, "The Password should be equal at most 8 digit  Please Re-enter The Password");
            } else if (!cpass.equals(pass)) {
                JOptionPane.showMessageDialog(rootPane, "The Confirm Password does not match the password  Please Re-enter The Password");
            } else {
                JOptionPane.showMessageDialog(rootPane, "The Phone Number should be equal 10 digit  Please Re-enter The Phone Number");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Please Fill The Fields To Set Up The Account");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

